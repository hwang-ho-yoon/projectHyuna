package com.hyuna.vo;

public class TermsVO {
	private int ter_no;
	private String ter_title;
	private String ter_content;
	private String ter_useCheck;
	private String ter_date;
	
	public int getTer_no() {
		return ter_no;
	}
	public void setTer_no(int ter_no) {
		this.ter_no = ter_no;
	}
	public String getTer_title() {
		return ter_title;
	}
	public void setTer_title(String ter_title) {
		this.ter_title = ter_title;
	}
	public String getTer_content() {
		return ter_content;
	}
	public void setTer_content(String ter_content) {
		this.ter_content = ter_content;
	}
	public String getTer_useCheck() {
		return ter_useCheck;
	}
	public void setTer_useCheck(String ter_useCheck) {
		this.ter_useCheck = ter_useCheck;
	}
	public String getTer_date() {
		return ter_date;
	}
	public void setTer_date(String ter_date) {
		this.ter_date = ter_date;
	}
}
